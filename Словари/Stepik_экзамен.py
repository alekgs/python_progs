p = {}
for _ in range(int(input())):
    client, goods, count = input().split()
    p.setdefault(client, {})
    p[client][goods] = p[client].get(goods, 0) + int(count)

for k, v in sorted(p.items()):
    print(f'{k}:')
    print(*sorted([f'{g} {n}' for g, n in v.items()]), sep='\n')
