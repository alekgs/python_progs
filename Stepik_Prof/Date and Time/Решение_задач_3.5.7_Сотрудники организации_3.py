from datetime import datetime, timedelta

mask, d = '%d.%m.%Y', {}
now = datetime.strptime(input(), mask)
start, end = now + timedelta(days=1), now + timedelta(days=7)

for _ in range(int(input())):
    name, bd = input().rsplit(' ', 1)
    b_day = datetime.strptime(bd, mask)
    d1 = b_day.replace(year=now.year)
    d2 = d1.replace(year=d1.year + 1)
    if (start < d1 <= end) or (start < d2 <= end):
        d[b_day] = name

